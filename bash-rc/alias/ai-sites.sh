# === Function to open multiple AI sites at once ===
ais() {
    case $1 in
        chat)
            /apps/firefox/firefox -P ravi -no-remote https://chat.openai.com https://claude.ai https://gemini.google.com &
            ;;
        image)
            /apps/firefox/firefox -P ravi -no-remote https://midjourney.com https://leonardo.ai https://playgroundai.com &
            ;;
        code)
            /apps/firefox/firefox -P ravi -no-remote https://github.com/features/copilot https://cursor.sh https://phind.com &
            ;;
        all)
            /apps/firefox/firefox -P ravi -no-remote \
                https://chat.openai.com \
                https://claude.ai \
                https://gemini.google.com \
                https://perplexity.ai \
                https://midjourney.com \
                https://github.com/features/copilot &
            ;;
        *)
            echo "Usage: ais [chat|image|code|all]"
            ;;
    esac
}

# === Quick open with specific profile (if you have multiple Firefox profiles) ===
aif() {
    local site=$1
    local profile=${2:-"ravi"}

    case $site in
        gpt) firefox -P "$profile" https://chat.openai.com & ;;
        claude) firefox -P "$profile" https://claude.ai & ;;
        gemini) firefox -P "$profile" https://gemini.google.com & ;;
        perplexity) firefox -P "$profile" https://www.perplexity.ai & ;;
        midjourney) firefox -P "$profile" https://www.midjourney.com & ;;
        dalle) firefox -P "$profile" https://labs.openai.com & ;;
        help) echo "Available: gpt, claude, gemini, perplexity, midjourney, dalle" ;;
        *) echo "Unknown site. Use 'aif help' for options." ;;
    esac
}
